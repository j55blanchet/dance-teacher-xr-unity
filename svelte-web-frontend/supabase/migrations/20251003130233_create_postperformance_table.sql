CREATE OR REPLACE FUNCTION public.prevent_user_supplied_id_user_learning_model()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
BEGIN
    -- If client supplied an id (non-null), override it. If NULL (no value supplied),
    -- the DEFAULT expression has already populated NEW.id before this trigger fires.
    IF TG_OP = 'INSERT' AND NEW.id IS NOT NULL THEN
        -- Always ensure the id is server-generated (idempotent if it already was)
        NEW.id = gen_random_uuid();
    END IF;
    RETURN NEW;
END;
$function$
