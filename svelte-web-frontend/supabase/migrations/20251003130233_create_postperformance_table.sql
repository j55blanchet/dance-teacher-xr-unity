CREATE TRIGGER enforce_id_ai_postperformance_action_ins
    BEFORE INSERT ON public.ai_postperformance_action
    FOR EACH ROW
    EXECUTE FUNCTION public.enforce_id_ai_postperformance_action();

DROP TRIGGER IF EXISTS enforce_id_ai_postperformance_action_upd ON public.ai_postperformance_action;
CREATE TRIGGER enforce_id_ai_postperformance_action_upd
    BEFORE UPDATE OF id ON public.ai_postperformance_action
    FOR EACH ROW
    EXECUTE FUNCTION public.enforce_id_ai_postperformance_action();

