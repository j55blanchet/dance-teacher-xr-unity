CREATE OR REPLACE FUNCTION public.enforce_id_ai_postperformance_action()
RETURNS trigger
LANGUAGE plpgsql
AS $function$
BEGIN
    IF TG_OP = 'INSERT' THEN
        -- If client supplied an id, override with next identity value
        IF NEW.id IS NOT NULL THEN
            NEW.id := nextval(pg_get_serial_sequence('public.ai_postperformance_action','id'));
        END IF;
    ELSIF TG_OP = 'UPDATE' THEN
        -- Prevent id changes on update
        IF NEW.id IS DISTINCT FROM OLD.id THEN
            NEW.id := OLD.id;
        END IF;
    END IF;
    RETURN NEW;
END;
$function$;

